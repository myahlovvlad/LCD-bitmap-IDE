export class FontRenderer {
  constructor(private readonly fontGlyphs: FontGlyphs = glyphs) {}

  hasGlyph(char: string, variant: FontVariantKey = '1'): boolean {
    const glyphSet = this.fontGlyphs[variant];
    return Boolean(
      glyphSet[char] ??
      glyphSet[char.toLowerCase()] ??
      glyphSet[char.toUpperCase()]
    );
  }

  canRenderText(text: string, variant: FontVariantKey = '1'): boolean {
    return Array.from(text).every((char) => this.hasGlyph(char, variant));
  }

  getGlyph(char: string, variant: FontVariantKey = '1'): Glyph {
    const glyphSet = this.fontGlyphs[variant];
    return normalizeGlyph(
      glyphSet[char] ??
      glyphSet[char.toLowerCase()] ??
      glyphSet[char.toUpperCase()] ??
      glyphSet[' '],
      variant
    );
  }

  measureText(text: string, variant: FontVariantKey = '1'): number {
    let width = 0;
    for (const char of text) {
      width += this.getGlyph(char, variant).width + 1;
    }

    return Math.max(0, width - 1);
  }

  renderTextBitmask(text: string, variant: FontVariantKey = '1'): boolean[][] {
    const width = this.measureText(text, variant);
    const glyphList = Array.from(text).map((char) => this.getGlyph(char, variant));
    const height = glyphList.reduce(
      (max, glyph) => Math.max(max, getGlyphBoxHeight(glyph, variant)),
      0
    );
    const maxTopOffset = glyphList.reduce(
      (max, glyph) => Math.max(max, glyph.topOffset ?? inferTopOffset(glyph, variant)),
      0
    );
    const maxNominalHeight = glyphList.reduce(
      (max, glyph) => Math.max(max, glyph.nominalHeight ?? inferNominalHeight(glyph, variant)),
      0
    );
    const bitmask = Array.from({ length: height }, () => Array.from({ length: width }, () => false));

    let cursorX = 0;
    for (const glyph of glyphList) {
      const topOffset = glyph.topOffset ?? inferTopOffset(glyph, variant);
      const nominalHeight = glyph.nominalHeight ?? inferNominalHeight(glyph, variant);
      const offsetY = Math.max(0, maxNominalHeight + maxTopOffset - nominalHeight - topOffset);
      glyph.data.forEach((row, y) => {
        for (let x = 0; x < row.length; x += 1) {
          if (bitmask[y + offsetY]) {
            bitmask[y + offsetY][cursorX + x] = row[x] === '#';
          }
        }
      });
      cursorX += glyph.width + 1;
    }

    return bitmask;
  }
}

export const defaultFontRenderer = new FontRenderer();

export function resolveLocalizedBitmapText(
  text: LocalizedText,
  language: LanguageCode,
  fontRenderer: FontRenderer = defaultFontRenderer,
  variant: FontVariantKey = '1'
): string {
  const languages = uniqueLanguages([language, 'ru', 'en', 'zh']);
  for (const candidate of languages) {
    const value = text[candidate];
    if (value && fontRenderer.canRenderText(value, variant)) {
      return value;
    }
  }

  return text[language] ?? text.ru ?? text.en ?? '';
}

export function createMutableFontGlyphs(): FontGlyphs {
  return JSON.parse(JSON.stringify(glyphs)) as FontGlyphs;
}

export function normalizeGlyph(glyph: Glyph, variant: FontVariantKey = '1'): Glyph {
  return {
    ...glyph,
    width: Math.max(glyph.width, glyph.data.reduce((max, row) => Math.max(max, row.length), 0)),
    topOffset: glyph.topOffset ?? inferTopOffset(glyph, variant),
    nominalHeight: glyph.nominalHeight ?? inferNominalHeight(glyph, variant)
  };
}

function getGlyphBoxHeight(glyph: Glyph, variant: FontVariantKey): number {
  const topOffset = glyph.topOffset ?? inferTopOffset(glyph, variant);
  const nominalHeight = glyph.nominalHeight ?? inferNominalHeight(glyph, variant);
  return Math.max(glyph.data.length, topOffset + nominalHeight);
}

function inferTopOffset(glyph: Glyph, variant: FontVariantKey): number {
  const defaultNominal = variant === '1' ? 7 : 8;
  return Math.max(0, glyph.data.length - defaultNominal);
}

function inferNominalHeight(glyph: Glyph, variant: FontVariantKey): number {
  const defaultNominal = variant === '1' ? 7 : 8;
  return Math.min(defaultNominal, Math.max(1, glyph.data.length));
}

function uniqueLanguages(languages: readonly LanguageCode[]): LanguageCode[] {
  return Array.from(new Set(languages));
}
